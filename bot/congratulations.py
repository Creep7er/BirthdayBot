def make_mention(birthday: Birthday) -> str:
    return (
        f'<a href="tg://user?id={birthday.telegram_user_id}">'
        f"{escape(birthday.display_name)}</a>"
    )


def build_congratulation(birthdays: list[Birthday]) -> str:
    mentions = [make_mention(birthday) for birthday in birthdays]
    if len(mentions) == 1:
        return random.choice(SINGLE_MESSAGES).format(mention=mentions[0])
    return random.choice(GROUP_MESSAGES).format(names=format_names(mentions))


def format_names(names: list[str]) -> str:
    if len(names) == 1:
        return names[0]
    if len(names) == 2:
        return f"{names[0]} и {names[1]}"
    return f"{', '.join(names[:-1])} и {names[-1]}"
